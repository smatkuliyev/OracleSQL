-- ROUND 
SELECT  ROUND(1234.5678,0),
        ROUND(1234.5678), 
        ROUND(1234.5678,1),
        ROUND(1234.5678,2),
        ROUND(1234.5678,-1),
        ROUND(1234.5678,-2)
FROM DUAL;

SELECT LEVEL-1,ROUND(1234.5678,LEVEL-1) 
FROM DUAL CONNECT BY LEVEL <6;

SELECT LEVEL-3,ROUND(1234.5678,LEVEL-3) 
FROM DUAL CONNECT BY LEVEL <8;

SELECT LEVEL-1, 'ROUND(1234.5678,' || TO_CHAR(LEVEL-1) || ')',
ROUND(1234.5678,LEVEL-1) 
FROM DUAL CONNECT BY LEVEL <6;

SELECT -LEVEL+1, 'ROUND(1234.5678,' || TO_CHAR(-LEVEL+1) || ')',
ROUND(1234.5678,-LEVEL+1) 
FROM DUAL CONNECT BY LEVEL <6;





