-- NUMERICAL FUNCTIONS 

-- SIGN
SELECT SIGN(-3), SIGN(3) FROM DUAL;

SELECT salary, SIGN(salary) FROM employees;

-- ABS
SELECT ABS(-15), ABS(1201), ABS(-154.54) FROM DUAL;

SELECT SIGN(-3*5+1), ABS(-4*3+8), ABS(6-9) FROM DUAL;

SELECT SIGN(-3*5+1-ABS(-4*3+8)), ABS(-4*3+8), ABS(6-9+SIGN(-8)) FROM DUAL;

--TRIGONOMETRIC (COS,ACOS,COSH,SIN,ASIN,SINH,TAN,ATAN,TANH)
 SELECT COS(30*3.1415/180) FROM DUAL; --RADIAN OF 30 DEGREE, PI=22/7;
 
 SELECT SIN(30*(22/7)/180) FROM DUAL; --RADIAN DEGREE
 
SELECT COS(0) FROM DUAL;
SELECT SIN(0) FROM DUAL;

SELECT TAN(45 * (22/7)/180) FROM DUAL; --RADIAN DEGREE

-- -1<=COS,SIN<=1               Y=COS(X), X=ArcCos(Y)

SELECT ACOS(1), COS(0) FROM DUAL;

SELECT ACOS(1/2) FROM DUAL;

-- CEIL, FLOOR
SELECT CEIL(123.4156948), CEIL(-5.6), FLOOR(123.4156948), FLOOR(-5.6) FROM DUAL;

-- EXP, LN          y=Ln(x) vs x=e^y
SELECT EXP(1), EXP(2) FROM DUAL;

SELECT LN(1),LN(2) FROM DUAL;

SELECT LN(EXP(1)) FROM DUAL;

